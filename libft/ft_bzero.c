#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	size_t			x;
	unsigned char	*d;

	x = 0;
	d = s;
	while (x < n)
	{
		d[x] = '\0';
		x++;
	}
	return (s);
}
